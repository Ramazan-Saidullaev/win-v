#!/bin/bash
# Скрипт для остановки всех процессов clipboard_history.py

echo "Остановка приложения истории буфера обмена..."

# Находим и убиваем все процессы
pkill -f "clipboard_history.py"

if [ $? -eq 0 ]; then
    echo "✓ Приложение остановлено"
else
    echo "⚠ Процессы не найдены или уже остановлены"
fi

